def load_window_df(window_s: int) -> pd.DataFrame:
    path = os.path.join(DATA_DIR, f"early_{window_s}s_enriched.csv")
    df = pd.read_csv(path)

    # Basic cleanup
    # Keep only needed columns if present
    keep_cols = ["file", "Cycle", LABEL_COL] + NUMERIC_FEATURES + CATEGORICAL_FEATURES
    keep_cols = [c for c in keep_cols if c in df.columns]
    df = df[keep_cols].copy()

    df[LABEL_COL] = df[LABEL_COL].astype(str)

    # Remove obviously invalid rows (optional)
    # Example: drop rows with missing label
    df = df.dropna(subset=[LABEL_COL])

    return df

def add_cycle_relative_features(df):
    df = df.copy()

    # sort to ensure correct first sample
    df = df.sort_values(["file", "Cycle", "t_cum_s"])

    # compute per-cycle baseline
    first_rows = (
        df.groupby(["file", "Cycle"])
        .first()
        .reset_index()[["file", "Cycle",
                        "voltage_v",
                        "capacity_ah",
                        "energy_wh",
                        "power_w"]]
        .rename(columns={
            "voltage_v": "V0",
            "capacity_ah": "Cap0",
            "energy_wh": "E0",
            "power_w": "P0"
        })
    )

    df = df.merge(first_rows, on=["file", "Cycle"], how="left")

    # delta features
    df["voltage_delta"] = df["voltage_v"] - df["V0"]
    df["capacity_delta"] = df["capacity_ah"] - df["Cap0"]
    df["energy_delta"] = df["energy_wh"] - df["E0"]
    df["power_delta"] = df["power_w"] - df["P0"]

    # ratio features (safe division)
    df["voltage_ratio"] = df["voltage_v"] / (df["V0"] + 1e-6)
    df["energy_ratio"] = df["energy_wh"] / (df["E0"] + 1e-6)

    return df



def stratified_file_split(df_ref: pd.DataFrame, test_size=0.30, random_state=42):
    """
    Split by file, stratified by each file's dominant class.
    Use a reference df (ideally 60s) so split is consistent across windows.
    """
    file_counts = (
        df_ref.groupby("file")[LABEL_COL]
        .value_counts()
        .unstack(fill_value=0)
    )
    file_counts["dominant_class"] = file_counts.idxmax(axis=1)
    file_info = file_counts.reset_index()[["file", "dominant_class"]]

    train_files, test_files = train_test_split(
        file_info["file"].values,
        test_size=test_size,
        random_state=random_state,
        stratify=file_info["dominant_class"].values
    )
    return set(train_files), set(test_files), file_counts


def build_model(numeric_features, categorical_features):
    numeric_transformer = Pipeline(
        steps=[
            ("imputer", SimpleImputer(strategy="median")),
        ]
    )

    categorical_transformer = Pipeline(
        steps=[
            ("imputer", SimpleImputer(strategy="most_frequent")),
            ("onehot", OneHotEncoder(handle_unknown="ignore")),
        ]
    )

    preprocessor = ColumnTransformer(
        transformers=[
            ("num", numeric_transformer, numeric_features),
            ("cat", categorical_transformer, categorical_features),
        ],
        remainder="drop",
        verbose_feature_names_out=False,
    )

    rf = RandomForestClassifier(
        n_estimators=500,
        random_state=RANDOM_STATE,
        n_jobs=-1,
        class_weight="balanced",
        max_depth=None,
        min_samples_leaf=1,
    )

    model = Pipeline(
        steps=[
            ("prep", preprocessor),
            ("rf", rf),
        ]
    )
    return model


def cycle_level_metrics(df_test: pd.DataFrame, proba: np.ndarray, classes: np.ndarray):
    """
    Convert datapoint predictions -> cycle predictions by averaging probabilities
    per (file, Cycle).
    """
    tmp = df_test[["file", "Cycle", LABEL_COL]].copy()
    for i, c in enumerate(classes):
        tmp[f"p_{c}"] = proba[:, i]

    grp = tmp.groupby(["file", "Cycle"], as_index=False)
    prob_cols = [f"p_{c}" for c in classes]
    cy = grp[prob_cols].mean()

    # true label per cycle (first)
    y_true_cycle = grp[LABEL_COL].first()[LABEL_COL].values
    y_pred_cycle = classes[np.argmax(cy[prob_cols].values, axis=1)]

    return {
        "cycle_accuracy": float(accuracy_score(y_true_cycle, y_pred_cycle)),
        "cycle_macro_f1": float(f1_score(y_true_cycle, y_pred_cycle, average="macro")),
        "cycle_cm": confusion_matrix(y_true_cycle, y_pred_cycle, labels=classes).tolist(),
    }

