def cycle_fuse_predictions(df_test, proba, classes, method="mean"):
    tmp = df_test[["file","Cycle","label"]].copy()
    for i, c in enumerate(classes):
        tmp[f"p_{c}"] = proba[:, i]

    prob_cols = [f"p_{c}" for c in classes]

    if method == "mean":
        agg = tmp.groupby(["file","Cycle"], as_index=False)[prob_cols].mean()
    elif method == "logsum":
        # sum log-probabilities across datapoints (more robust than mean)
        eps = 1e-9
        tmp_log = tmp.copy()
        for col in prob_cols:
            tmp_log[col] = np.log(tmp_log[col] + eps)
        agg = tmp_log.groupby(["file","Cycle"], as_index=False)[prob_cols].sum()
    else:
        raise ValueError("method must be 'mean' or 'logsum'")

    # true label per cycle
    y_true = tmp.groupby(["file","Cycle"])["label"].first().values
    y_pred = classes[np.argmax(agg[prob_cols].values, axis=1)]

    return {
        "cycle_acc": float(accuracy_score(y_true, y_pred)),
        "cycle_macro_f1": float(f1_score(y_true, y_pred, average="macro")),
        "cycle_cm": confusion_matrix(y_true, y_pred, labels=classes),
    }

def cycle_fuse_predictions_topk(df_test, proba, classes, k=25, score_by="min_voltage", method="logsum"):
    """
    Fuse datapoint predictions into cycle prediction using only top-k informative datapoints per cycle.

    score_by:
      - "min_voltage": pick k lowest voltage_v points per (file,Cycle)
      - "max_abs_dvdt": pick k largest |dvdt_v_per_s| points per (file,Cycle)
      - "first_k": pick first k points by t_cum_s
    method:
      - "logsum" recommended
      - "mean" supported
    """
    tmp = df_test[["file", "Cycle", "label", "t_cum_s", "voltage_v", "dvdt_v_per_s"]].copy()

    for i, c in enumerate(classes):
        tmp[f"p_{c}"] = proba[:, i]

    prob_cols = [f"p_{c}" for c in classes]

    # choose top-k rows per cycle
    def pick_group(g):
        if score_by == "min_voltage":
            g2 = g.sort_values("voltage_v", ascending=True)
        elif score_by == "max_abs_dvdt":
            g2 = g.assign(_s=np.abs(g["dvdt_v_per_s"])).sort_values("_s", ascending=False)
        elif score_by == "first_k":
            g2 = g.sort_values("t_cum_s", ascending=True)
        else:
            raise ValueError("score_by must be one of: min_voltage, max_abs_dvdt, first_k")
        return g2.head(k)

    tmp = tmp.groupby(["file", "Cycle"], group_keys=False).apply(pick_group)

    if method == "mean":
        agg = tmp.groupby(["file", "Cycle"], as_index=False)[prob_cols].mean()
    elif method == "logsum":
        eps = 1e-9
        tmp_log = tmp.copy()
        for col in prob_cols:
            tmp_log[col] = np.log(tmp_log[col] + eps)
        agg = tmp_log.groupby(["file", "Cycle"], as_index=False)[prob_cols].sum()
    else:
        raise ValueError("method must be 'mean' or 'logsum'")

    y_true = tmp.groupby(["file", "Cycle"])["label"].first().values
    y_pred = classes[np.argmax(agg[prob_cols].values, axis=1)]

    return {
        "cycle_acc": float(accuracy_score(y_true, y_pred)),
        "cycle_macro_f1": float(f1_score(y_true, y_pred, average="macro")),
        "cycle_cm": confusion_matrix(y_true, y_pred, labels=classes),
    }


